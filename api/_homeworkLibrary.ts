// ==================== ГЕНЕРАЦІЯ ПРОПОЗИЦІЙ ====================

function clamp(value: number, min: number, max: number) {
  return Math.max(min, Math.min(max, value));
}

function inferDifficultyFromGroupName(value?: string): HomeworkDifficulty | null {
  const groupName = String(value || '').toLowerCase();
  if (!groupName) return null;
  if (groupName.includes('старш') || groupName.includes('дорос') || groupName.includes('12+') || groupName.includes('advanced')) return 'hard';
  if (groupName.includes('серед') || groupName.includes('middle') || groupName.includes('8+') || groupName.includes('9+') || groupName.includes('10+')) return 'medium';
  if (groupName.includes('молод') || groupName.includes('новач') || groupName.includes('почат') || groupName.includes('kids')) return 'easy';
  return null;
}

export function getHomeworkLibrarySummaryFromLibrary(library: HomeworkLibraryExercise[] = HOMEWORK_LIBRARY) {
  const focusCounts = library.reduce<Record<string, number>>((acc, item) => {
    acc[item.focus] = (acc[item.focus] || 0) + 1;
    return acc;
  }, {});

  return {
    count: library.length,
    focusCounts,
    focuses: Object.entries(homeworkFocusLabels).map(([id, label]) => ({
      id,
      label,
      count: focusCounts[id] || 0,
    })),
  };
}

export function getHomeworkLibrarySummary() {
  return getHomeworkLibrarySummaryFromLibrary(HOMEWORK_LIBRARY);
}

function buildSuggestion(
  title: string,
  focus: HomeworkFocus,
  difficulty: HomeworkDifficulty,
  minutes: number,
  goal: string,
  parentCheck: string,
  exercises: HomeworkLibraryExercise[],
) {
  const config = difficultyConfig[difficulty];
  return {
    title,
    description: `${goal} Для кого: ${difficultyAudience[difficulty]}. Час: ~${minutes} хв. Як перевірити вдома: ${parentCheck}`,
    focus,
    difficulty,
    recommended_for: difficultyAudience[difficulty],
    estimated_minutes: minutes,
    exercises,
    coach_note: `Перевірка: щоденник, стабільність форми і чесний коментар учня — не кількість заради кількості. Рекомендовані бали: ${config.points}.`,
  };
}

// Запасний варіант для бібліотеки з Google Sheets, де немає наших id.
// У таблиці кожен рядок — уже готовий комплекс, тому одна пропозиція = один рядок.
function buildFallbackSuggestions(
  library: HomeworkLibraryExercise[],
  focus: HomeworkFocus,
  difficulty: HomeworkDifficulty,
  minutes: number,
  count: number,
) {
  const exactLevel: Record<HomeworkDifficulty, HomeworkLibraryExercise['level']> = {
    easy: 'beginner',
    medium: 'intermediate',
    hard: 'advanced',
  };
  const exact = library.filter(item => item.focus === focus && item.level === exactLevel[difficulty]);
  const allowedLevels = difficultyLevels[difficulty];
  const widened = exact.length > 0
    ? exact
    : library.filter(item => item.focus === focus && allowedLevels.includes(item.level));
  const pool = widened.length > 0 ? widened : library.filter(item => item.focus === focus);

  return pool.slice(0, count).map(exercise => buildSuggestion(
    exercise.name,
    focus,
    difficulty,
    minutes,
    `${exercise.target}.`,
    'пройдіться разом по полях «Ключі техніки» і «Типові помилки» та звірте виконання.',
    [exercise],
  ));
}

export function generateHomeworkSuggestionsFromLibrary(library: HomeworkLibraryExercise[], params: any) {
  const focus = (['technique', 'kata', 'conditioning', 'flexibility', 'discipline'].includes(String(params?.focus))
    ? String(params.focus)
    : 'technique') as HomeworkFocus;
  const inferredDifficulty = inferDifficultyFromGroupName(params?.group_name);
  const difficulty = (['easy', 'medium', 'hard'].includes(String(params?.difficulty))
    ? String(params.difficulty)
    : inferredDifficulty || 'medium') as HomeworkDifficulty;
  const minutes = clamp(Number(params?.estimated_minutes) || 15, 8, 60);
  const count = clamp(Number(params?.count) || 12, 3, 24);

  const byId = new Map(library.map(item => [item.id, item]));

  const matching = HOMEWORK_COMPLEXES
    .filter(complex => complex.focus === focus && complex.difficulty === difficulty)
    // Найближчі за часом до запиту тренера — першими.
    .sort((a, b) => Math.abs(a.minutes - minutes) - Math.abs(b.minutes - minutes));

  const resolved = matching
    .map(complex => {
      const exercises = complex.exerciseIds
        .map(id => byId.get(id))
        .filter((item): item is HomeworkLibraryExercise => Boolean(item));
      if (exercises.length === 0) return null;
      return buildSuggestion(
        complex.title,
        focus,
        difficulty,
        complex.minutes,
        complex.goal,
        complex.parentCheck,
        exercises,
      );
    })
    .filter((item): item is NonNullable<ReturnType<typeof buildSuggestion>> => Boolean(item));

  // Бібліотеку могли замінити через Google Sheets — тоді наших id там немає
  // і комплекси не зберуться. Збираємо запасні комплекси з того, що є.
  if (resolved.length === 0) {
    return buildFallbackSuggestions(library, focus, difficulty, minutes, count);
  }

  return resolved.slice(0, count);
}

export function generateHomeworkSuggestions(params: any) {
  return generateHomeworkSuggestionsFromLibrary(HOMEWORK_LIBRARY, params);
}
