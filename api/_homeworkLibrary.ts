const generatedSetByLevel: Record<HomeworkLibraryExercise['level'], number> = {
  beginner: 3,
  intermediate: 4,
  advanced: 5,
};

const generatedLevelCycle: HomeworkLibraryExercise['level'][] = ['beginner', 'intermediate', 'advanced'];

function buildGeneratedHomeworkExercises(existing: HomeworkLibraryExercise[]): HomeworkLibraryExercise[] {
  const existingCounts = existing.reduce<Record<HomeworkFocus, number>>((acc, item) => {
    acc[item.focus] = (acc[item.focus] || 0) + 1;
    return acc;
  }, { technique: 0, kata: 0, conditioning: 0, flexibility: 0, discipline: 0 });

  return (Object.keys(homeworkFocusLabels) as HomeworkFocus[]).flatMap((focus) => {
    const plans = generatedHomeworkPlans[focus];
    const needed = Math.max(0, homeworkTargetPerFocus - (existingCounts[focus] || 0));

    return Array.from({ length: needed }).map((_, index) => {
      const plan = plans[index % plans.length];
      const level = generatedLevelCycle[Math.floor(index / Math.max(1, Math.ceil(needed / generatedLevelCycle.length))) % generatedLevelCycle.length];
      const wave = Math.floor(index / plans.length) + 1;
      const variant = index + 1;
      const intensity = level === 'advanced'
        ? 'досвідчений рівень: сильніший темп, самоконтроль і чесний щоденник'
        : level === 'intermediate'
          ? 'середній рівень: стабільна техніка і рівний темп'
          : 'базовий рівень: спокійне виконання без гонки';

      return {
        id: `generated_${focus}_${String(variant).padStart(3, '0')}`,
        focus,
        level,
        equipment: plan.equipment,
        name: `${plan.name} ${wave}`,
        target: plan.target,
        sets: generatedSetByLevel[level],
        reps: plan.baseReps,
        rest: plan.rest,
        note: `${plan.note} ${intensity}.`,
        explanation: plan.explanation,
        cues: plan.cues,
        mistakes: plan.mistakes,
        safety: plan.safety,
        progression: plan.progression,
        diary_prompt: plan.diary_prompt,
      };
    });
  });
}

