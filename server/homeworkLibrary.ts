function clamp(value: number, min: number, max: number) {
  return Math.max(min, Math.min(max, value));
}

function seededRandom(seed: number) {
  let value = seed || 1;
  return () => {
    value = (value * 1664525 + 1013904223) % 4294967296;
    return value / 4294967296;
  };
}

function rotatePick<T>(items: T[], start: number, count: number) {
  const picked: T[] = [];
  for (let index = 0; index < count; index += 1) {
    picked.push(items[(start + index) % items.length]);
  }
  return picked;
}

function adjustExercise(exercise: HomeworkLibraryExercise, difficulty: keyof typeof difficultyConfig, index: number) {
  const config = difficultyConfig[difficulty];
  return {
    ...exercise,
    id: `${exercise.id}_${index}`,
    sets: clamp(Number(exercise.sets || 1) + config.setDelta, 1, 8),
  };
}

function inferDifficultyFromGroupName(value?: string) {
  const groupName = String(value || '').toLowerCase();
  if (!groupName) return null;
  if (groupName.includes('старш') || groupName.includes('дорос') || groupName.includes('12+') || groupName.includes('advanced')) return 'hard';
  if (groupName.includes('серед') || groupName.includes('middle') || groupName.includes('8+') || groupName.includes('9+') || groupName.includes('10+')) return 'medium';
  if (groupName.includes('молод') || groupName.includes('новач') || groupName.includes('почат') || groupName.includes('kids')) return 'easy';
  return null;
}

export function getHomeworkLibrarySummary() {
  const focusCounts = HOMEWORK_LIBRARY.reduce<Record<string, number>>((acc, item) => {
    acc[item.focus] = (acc[item.focus] || 0) + 1;
    return acc;
  }, {});

  return {
    count: HOMEWORK_LIBRARY.length,
    focusCounts,
    focuses: Object.entries(homeworkFocusLabels).map(([id, label]) => ({
      id,
      label,
      count: focusCounts[id] || 0,
    })),
  };
}

export function generateHomeworkSuggestions(params: any) {
  const focus = (['technique', 'kata', 'conditioning', 'flexibility', 'discipline'].includes(String(params?.focus))
    ? String(params.focus)
    : 'technique') as HomeworkFocus;
  const inferredDifficulty = inferDifficultyFromGroupName(params?.group_name);
  const difficulty = (['easy', 'medium', 'hard'].includes(String(params?.difficulty))
    ? String(params.difficulty)
    : inferredDifficulty || 'medium') as keyof typeof difficultyConfig;
  const minutes = clamp(Number(params?.estimated_minutes) || 15, 8, 60);
  const variantCount = clamp(Number(params?.count) || 12, 3, 24);
  const seed = Number(params?.seed) || Date.now();
  const random = seededRandom(seed);
  const allowedLevels = difficultyLevels[difficulty];
  const rawMainPool = HOMEWORK_LIBRARY.filter(item => item.focus === focus);
  const mainPool = rawMainPool.filter(item => allowedLevels.includes(item.level));
  const rawSupportPool = HOMEWORK_LIBRARY.filter(item => item.focus !== focus && (item.focus === 'discipline' || item.focus === 'flexibility'));
  const supportPool = rawSupportPool.filter(item => allowedLevels.includes(item.level));
  const effectiveMainPool = mainPool.length > 0 ? mainPool : rawMainPool;
  const effectiveSupportPool = supportPool.length > 0 ? supportPool : rawSupportPool;
  const basePerSuggestion = minutes >= 35 ? 5 : minutes >= 22 ? 4 : 3;
  const perSuggestion = difficulty === 'hard'
    ? Math.max(4, basePerSuggestion)
    : difficulty === 'easy'
      ? Math.min(3, basePerSuggestion)
      : basePerSuggestion;
  const config = difficultyConfig[difficulty];

  return Array.from({ length: variantCount }).map((_, offset) => {
    const start = Math.floor(random() * effectiveMainPool.length + offset * 2) % effectiveMainPool.length;
    let exercises = rotatePick(effectiveMainPool, start, perSuggestion);

    if (focus !== 'discipline' && offset % 4 === 2 && effectiveSupportPool.length > 0) {
      const support = effectiveSupportPool[(start + offset) % effectiveSupportPool.length];
      exercises = [...exercises.slice(0, Math.max(1, perSuggestion - 1)), support];
    }

    const normalizedExercises = exercises.map((exercise, index) => adjustExercise(exercise, difficulty, index + 1));
    const prefix = titlePrefixes[(offset + Math.floor(random() * titlePrefixes.length)) % titlePrefixes.length];
    const focusLabel = homeworkFocusLabels[focus];
    const targetLine = normalizedExercises.map(item => item.name).slice(0, 3).join(' + ');
    const checkText = difficulty === 'hard'
      ? 'після виконання обовʼязково записати, що вийшло і де техніка почала ламатися'
      : difficulty === 'easy'
        ? 'виконати спокійно і записати один короткий висновок'
        : 'після виконання заповнити щоденник по сетах, часу і відчуттях';

    return {
      title: `${prefix}: ${focusLabel} ${offset + 1}`,
      description: `Домашнє завдання на ${minutes} хв: ${config.minutesText}. Рівень: ${difficultyAudience[difficulty]}. Акцент: ${targetLine}. Завдання: ${checkText}.`,
      focus,
      difficulty,
      recommended_for: difficultyAudience[difficulty],
      estimated_minutes: minutes,
      exercises: normalizedExercises,
      coach_note: `Перевірка: дивитися не на кількість заради кількості, а на щоденник, стабільність форми і чесний коментар учня. Рекомендовані бали: ${config.points}.`,
    };
  });
}
